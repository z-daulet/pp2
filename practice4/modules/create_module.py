def area_of_rectangle(a:int,b:int):
    return f"Area of rectangle: {a*b}"

def is_even(num: int):
    return f"Is even: {num % 2 == 0}"

def square_of_number(num: int):
    return f"Square: {num * num}"

def area_of_circle(radius: int):
    return f"Area of circle: {3.14 * radius * radius}"

def perimeter_of_circle(radius: int):
    return f"Perimeter of circle: {2 * 3.14 * radius}"
